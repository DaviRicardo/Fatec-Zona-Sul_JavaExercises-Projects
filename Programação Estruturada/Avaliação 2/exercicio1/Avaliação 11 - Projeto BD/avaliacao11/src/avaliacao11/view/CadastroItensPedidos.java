    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        try {
            itePedDao = new ItensPedidosDao();
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(CadastroItensPedidos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (jTextFieldCodigoProduto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O código do produto não pode ser nulo!");
            jTextFieldCodigoProduto.requestFocus();
        }
        if (jTextFieldCodigoPedido.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O código do pedido não pode ser nulo!");
            jTextFieldCodigoPedido.requestFocus();
        }
        if (jTextFieldValorTotal.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O valor total não pode ser nulo!");
            jTextFieldValorTotal.requestFocus();
        }
        
        itePedDao.itensped.setCodProd(Integer.parseInt(jTextFieldCodigoProduto.getText()));
        itePedDao.itensped.setCodPed(Integer.parseInt(jTextFieldCodigoPedido.getText()));
        itePedDao.itensped.setQtde(Float.parseFloat(jTextFieldValorTotal.getText()));
        
        JOptionPane.showMessageDialog(null, itePedDao.atualizar(ItensPedidosDao.INCLUSAO));
        limparCampos();
        tabelaItensPedidos();
        
    }//GEN-LAST:event_jButtonIncluirActionPerformed

