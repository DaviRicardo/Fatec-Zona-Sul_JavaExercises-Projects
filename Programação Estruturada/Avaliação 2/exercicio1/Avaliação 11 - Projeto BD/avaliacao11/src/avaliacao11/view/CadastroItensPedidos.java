        itePedDao.itensped.setCodProd(Integer.parseInt(jTextFieldCodigoProduto.getText()));
        itePedDao.itensped.setCodPed(Integer.parseInt(jTextFieldCodigoPedido.getText()));
        itePedDao.itensped.setQtde(Float.parseFloat(jTextFieldValorTotal.getText()));
        
