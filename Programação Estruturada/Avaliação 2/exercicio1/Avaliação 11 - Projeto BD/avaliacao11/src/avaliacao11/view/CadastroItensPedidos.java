    public void tabelaItensPedidos() {
        try {
            itePedDao = new ItensPedidosDao();
            String sql = """
                        select * from itensped 
                        inner join produtos on produtos.codProd = itensped.Produtos_codProd 
                        inner join pedidos on pedidos.codPed = itensped.Pedidos_codPed 
                         """;
            PreparedStatement stmt = itePedDao.c.prepareStatement(sql);
            rs = stmt.executeQuery();
        } catch (SQLException | ClassNotFoundException error) {
            JOptionPane.showMessageDialog(null, "Problemas na conexão!\n" + error.toString());
        }
    }
    
    public void atualizarCampos() {
        itePedDao.itensped.setCodItensPed(Integer.parseInt(jTextFieldId.getText()));
        if (itePedDao.localizar()) {
            // Itens - Pedidos
            jTextFieldId.setText(Integer.toString(itePedDao.itensped.getCodItensPed()));
            jTextFieldCodigoProduto.setText(Integer.toString(itePedDao.itensped.getCodProd()));
            jTextFieldCodigoPedido.setText(Integer.toString(itePedDao.itensped.getCodPed()));
            jTextFieldValorTotal.setText(Float.toString(itePedDao.itensped.getQtde()));
            // Produto
            jTextFieldDescricao.setText(itePedDao.produto.getDescricao());
            jTextFieldPreco.setText(Float.toString(itePedDao.produto.getPreco()));
            jTextFieldUnidade.setText(itePedDao.produto.getUnidade());
            jTextFieldQuantidadeInicial.setText(Float.toString(itePedDao.produto.getQtdeInicial()));
            jTextFieldDataCadastro.setText(converterData(itePedDao.produto.getDataCad().toString()));
            jTextFieldQuantidadeAtual.setText(Float.toString(itePedDao.produto.getQtdeAtual()));
            // Pedido
            jTextFieldDataPedido.setText(converterData(itePedDao.pedido.getData().toString()));
            setCamposTexto(true,true, true, false, false, false, false, false, false, false, false);
            setBotoes(true,true,false,true,true,true);
        } else {
            JOptionPane.showMessageDialog(null , "Registro não encontrado!", "Falha na localização", JOptionPane.WARNING_MESSAGE);
            limparCampos();
        }
    }
    
    public String converterData (String data) {
        // 2023-06-13
        String ano = data.substring(0, 4); // 2023
        String mes = data.substring(5, 7); // 06
        String dia = data.substring(8, 10); // 13
        String r = dia + "/" + mes + "/" + ano; // 13/06/2023
        return r;
    }
    
    public void carregaDados() {
        try {
            if (rs.isAfterLast()) {
                rs.last();
            } else if (rs.isBeforeFirst()) {
                rs.first();
            } 
            jTextFieldId.setText(Integer.toString(rs.getInt("codItensPed")));
            jTextFieldCodigoProduto.setText(Integer.toString(rs.getInt("Produtos_codProd")));
            jTextFieldCodigoPedido.setText(Integer.toString(rs.getInt("Pedidos_codPed")));
            jTextFieldValorTotal.setText(Float.toString(rs.getFloat("qtde")));
            // Produto
            jTextFieldDescricao.setText(rs.getString("descricao"));
            jTextFieldPreco.setText(Float.toString(rs.getFloat("preco")));
            jTextFieldUnidade.setText(rs.getString("unidade"));
            jTextFieldQuantidadeInicial.setText(Float.toString(rs.getFloat("qtdeInicial")));
            jTextFieldDataCadastro.setText(converterData(rs.getDate("dataCad").toString()));
            jTextFieldQuantidadeAtual.setText(Float.toString(rs.getFloat("qtdeAtual")));
            // Pedido
            jTextFieldDataPedido.setText(converterData(rs.getDate("data").toString()));
            setBotoes(true,true,false,true,true,true);
            setCamposTexto(true,true, true, false, false, false, false, false, false, false, false);
        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, "Erro na conexão!\n" + error.toString());
        }
    }

