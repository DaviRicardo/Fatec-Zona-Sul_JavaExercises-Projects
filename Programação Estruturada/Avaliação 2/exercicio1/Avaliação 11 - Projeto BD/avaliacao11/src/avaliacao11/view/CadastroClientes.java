    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        try {
            clienDao = new ClientesDao() ;
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(CadastroClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (jTextFieldNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O nome não pode ser vazio!");
            jTextFieldNome.requestFocus();
        }
        if (jTextFieldEndereco.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O endereço não pode ser vazio");
            jTextFieldEndereco.requestFocus();
        }
        if (jTextFieldBairro.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O bairro não pode ser vazio!");
            jTextFieldBairro.requestFocus();
        }
        if (jTextFieldCidade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "A cidade não pode ser vazia!");
            jTextFieldCidade.requestFocus();
        }
        if (jTextFieldCep.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O cep não pode ser vazio");
            jTextFieldCep.requestFocus();
        }
        if (jTextFieldUf.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "A UF não pode ser vazia!");
            jTextFieldUf.requestFocus();
        }
        if (jTextFieldEmail.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O email não pode ser vazio!");
            jTextFieldEmail.requestFocus();
        }
        if (jTextFieldCelular.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O número de celular não pode ser vazio!");
            jTextFieldCelular.requestFocus();
        }
        
        clienDao.cliente.setNome(jTextFieldNome.getText());
        clienDao.cliente.setEndereco(jTextFieldEndereco.getText());
        clienDao.cliente.setBairro(jTextFieldBairro.getText());
        clienDao.cliente.setCidade(jTextFieldCidade.getText());
        clienDao.cliente.setCep(jTextFieldCep.getText());
        clienDao.cliente.setUf(jTextFieldUf.getText());
        clienDao.cliente.setEmail(jTextFieldEmail.getText());
        clienDao.cliente.setFone(jTextFieldFone.getText());
        clienDao.cliente.setCelular(jTextFieldCelular.getText());
                
        JOptionPane.showMessageDialog(null, clienDao.atualizar(ClientesDao.INCLUSAO));
        limparCampos();
        tabelaClientes();
    }//GEN-LAST:event_jButtonIncluirActionPerformed

