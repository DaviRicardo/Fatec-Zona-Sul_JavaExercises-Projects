    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        try {
            clienDao = new ClientesDao() ;
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(CadastroClientes.class.getName()).log(Level.SEVERE, null, ex);
        }
        clienDao.cliente.setCodCliente(Integer.parseInt(jTextFieldId.getText()));
        clienDao.localizar();
        
        int confirmacao = JOptionPane.showConfirmDialog(null, clienDao.cliente.getNome(), "Excluir o cliente?", JOptionPane.YES_NO_OPTION);
        if (confirmacao == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(null, clienDao.atualizar(ClientesDao.EXCLUSAO)); 
        }
        limparCampos();
        tabelaClientes();
    }//GEN-LAST:event_jButtonExcluirActionPerformed

