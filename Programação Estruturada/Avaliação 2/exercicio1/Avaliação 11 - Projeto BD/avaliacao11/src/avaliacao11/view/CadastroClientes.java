    public void tabelaClientes () {
        try {
            clienDao = new ClientesDao();
            String sql = "select * from clientes";
            PreparedStatement stmt = clienDao.c.prepareStatement(sql);
            rs = stmt.executeQuery();
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Problemas na conexão!\n" + ex.toString());
        }
    }
    
    public void carregaDados() {
        try {
            if (rs.isAfterLast()) {
                rs.last();
            }
            if (rs.isBeforeFirst()) {
                rs.first();
            }
            
            jTextFieldId.setText(Integer.toString(rs.getInt("codCliente")));
            jTextFieldNome.setText(rs.getString("nome"));
            jTextFieldEndereco.setText(rs.getString("ender"));
            jTextFieldBairro.setText(rs.getString("bairro"));
            jTextFieldCidade.setText(rs.getString("cidade"));
            jTextFieldCep.setText(rs.getString("cep"));
            jTextFieldUf.setText(rs.getString("uf"));
            jTextFieldEmail.setText(rs.getString("email"));
            jTextFieldFone.setText(rs.getString("fone"));
            jTextFieldCelular.setText(rs.getString("celular"));
            setBotoes(true,true,false,true,true,true);
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Problemas na conexão!\n" + erro.toString());
        }
    }
    

