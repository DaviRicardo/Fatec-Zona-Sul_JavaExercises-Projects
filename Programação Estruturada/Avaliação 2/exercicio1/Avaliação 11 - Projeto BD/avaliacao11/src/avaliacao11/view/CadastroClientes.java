    public void limparCampos() {
        jTextFieldId.setEditable(false);
        count = 0;
        jTextFieldNome.requestFocus();
        jTextFieldId.setText("");
        jTextFieldNome.setText("");
        jTextFieldEndereco.setText("");
        jTextFieldBairro.setText("");
        jTextFieldCidade.setText("");
        jTextFieldCep.setText("");
        jTextFieldUf.setText("");
        jTextFieldEmail.setText("");
        jTextFieldFone.setText("");
        jTextFieldCelular.setText("");
        setBotoes(true,true,false,false,false,true);
    }
    
    public void atualizarCampos() {
        clienDao.cliente.setCodCliente(Integer.parseInt(jTextFieldId.getText()));
        if (clienDao.localizar()) {
            jTextFieldId.setText(Integer.toString(clienDao.cliente.getCodCliente()));
            jTextFieldNome.setText(clienDao.cliente.getNome());
            jTextFieldEndereco.setText(clienDao.cliente.getEndereco());
            jTextFieldBairro.setText(clienDao.cliente.getBairro());
            jTextFieldCidade.setText(clienDao.cliente.getCidade());
            jTextFieldCep.setText(clienDao.cliente.getCep());
            jTextFieldUf.setText(clienDao.cliente.getUf());
            jTextFieldEmail.setText(clienDao.cliente.getEmail());
            jTextFieldFone.setText(clienDao.cliente.getFone());
            jTextFieldCelular.setText(clienDao.cliente.getCelular());
            setBotoes(false,true,false,true,true,true);
        } else {
            JOptionPane.showMessageDialog(null, "Cliente não encontrado!", "Falha na localização", JOptionPane.ERROR_MESSAGE);
            limparCampos();
        }
    }

    
