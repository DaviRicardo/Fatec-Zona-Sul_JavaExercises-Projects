    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
        prodDao = new ProdutosDao();
        
        if (jTextFieldDescricao.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "A descrição não pode ser nula!");
            jTextFieldDescricao.requestFocus();
        }
        if (jTextFieldPreco.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O preço não pode ser nulo!");
            jTextFieldPreco.requestFocus();
        }
        if (jTextFieldUnidade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "A unidade não pode ser nula!");
            jTextFieldUnidade.requestFocus();
        }
        if (jTextFieldQuantidadeInicial.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "A quantidade inicial não pode ser nula!");
            jTextFieldQuantidadeInicial.requestFocus();
        }
        if (jTextFieldDataCadastro.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "A data de cadastro não pode ser nula!");
           jTextFieldDataCadastro.requestFocus();
        }
        if (jTextFieldQuantidadeAtual.getText().equals("")) {
           JOptionPane.showMessageDialog(null, "A quantidade atual não pode ser nula!");
           jTextFieldQuantidadeAtual.requestFocus();
        }
        
        prodDao.produto.setDescricao(jTextFieldDescricao.getText());
        prodDao.produto.setPreco(Float.parseFloat(jTextFieldPreco.getText()));
        prodDao.produto.setUnidade(jTextFieldUnidade.getText());
        prodDao.produto.setQtdeInicial(Float.parseFloat(jTextFieldQuantidadeInicial.getText()));
        prodDao.produto.setQtdeAtual(Float.parseFloat(jTextFieldQuantidadeAtual.getText()));
        
        Date data = new Date();
        String dataString = jTextFieldDataCadastro.getText();
        SimpleDateFormat format = new SimpleDateFormat ("dd/MM/yyyy");
        
        try {
            data = format.parse(dataString);
            java.sql.Date dataSql = new java.sql.Date (data.getTime()); // A passagem do valor de timestamp obtido por getTime() ao construtor de java.sql.Date permite criar um objeto java.sql.Date com a mesma data e hora de um objeto do tipo Date 
            prodDao.produto.setDataCad(dataSql);
        } catch (ParseException error) {
           Logger.getLogger(CadastroProdutos.class.getName()).log(Level.SEVERE, null, error);
        }
        
        JOptionPane.showMessageDialog(null, prodDao.atualizar(ProdutosDao.INCLUSAO));
        limparCampos();
        tabelaProdutos();
    }//GEN-LAST:event_jButtonIncluirActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        prodDao.produto.setCodProd(Integer.parseInt(jTextFieldId.getText()));
        prodDao.localizar();
        
        int confirmacao = JOptionPane.showConfirmDialog(null, prodDao.produto.getDescricao(), "Excluir o produto?", JOptionPane.YES_NO_OPTION);
        if (confirmacao == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(null, prodDao.atualizar(ProdutosDao.EXCLUSAO));
        }
        limparCampos();
        tabelaProdutos();
    }//GEN-LAST:event_jButtonExcluirActionPerformed

