    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        prodDao.produto.setCodProd(Integer.parseInt(jTextFieldId.getText()));
        prodDao.produto.setDescricao(jTextFieldDescricao.getText());
        prodDao.produto.setPreco(Float.parseFloat(jTextFieldPreco.getText()));
        prodDao.produto.setUnidade(jTextFieldUnidade.getText());
        prodDao.produto.setQtdeInicial(Float.parseFloat(jTextFieldQuantidadeInicial.getText()));
        prodDao.produto.setQtdeAtual(Float.parseFloat(jTextFieldQuantidadeAtual.getText()));
        
        Date data = new Date();
        String dataString = jTextFieldDataCadastro.getText();
        SimpleDateFormat format = new SimpleDateFormat ("dd/MM/yyyy");
        
        try {
            data = format.parse(dataString);
            java.sql.Date dataSql = new java.sql.Date (data.getTime()); // A passagem do valor de timestamp obtido por getTime() ao construtor de java.sql.Date permite criar um objeto java.sql.Date com a mesma data e hora de um objeto do tipo Date 
            prodDao.produto.setDataCad(dataSql);
        } catch (ParseException error) {
           Logger.getLogger(CadastroProdutos.class.getName()).log(Level.SEVERE, null, error);
        }
        
        JOptionPane.showMessageDialog(null, prodDao.atualizar(ProdutosDao.ALTERACAO));
        limparCampos();
        tabelaProdutos();        
    }//GEN-LAST:event_jButtonAlterarActionPerformed

