    public void limparCampos() {
        jTextFieldId.setEditable(false);
        count = 0;
        jTextFieldId.setText("");
        jTextFieldDescricao.setText("");
        jTextFieldPreco.setText("");
        jTextFieldUnidade.setText("");
        jTextFieldQuantidadeInicial.setText("");
        jTextFieldDataCadastro.setText("");
        jTextFieldQuantidadeAtual.setText("");
        jTextFieldDescricao.requestFocus();
        setBotoes(true, true, false, false, false, true);
    }
    
     public void atualizarCampos() {
        prodDao.produto.setCodProd(Integer.parseInt(jTextFieldId.getText()));
        if (prodDao.localizar()) {
            jTextFieldId.setText(Integer.toString(prodDao.produto.getCodProd()));
            jTextFieldDescricao.setText(prodDao.produto.getDescricao());
            jTextFieldPreco.setText(Float.toString(prodDao.produto.getPreco()));
            jTextFieldUnidade.setText(prodDao.produto.getUnidade());
            jTextFieldQuantidadeInicial.setText(Float.toString(prodDao.produto.getQtdeInicial()));
            jTextFieldDataCadastro.setText(converterData(prodDao.produto.getDataCad().toString()));
            jTextFieldQuantidadeAtual.setText(Float.toString(prodDao.produto.getQtdeAtual()));
            setBotoes (false, true, false, true, true, true);
        } else {
            JOptionPane.showMessageDialog(null, "Produto não encontrado!", "Falha na localização", JOptionPane.WARNING_MESSAGE);
            limparCampos();
        }
    }
            
    public void tabelaProdutos() {
        try {
            prodDao = new ProdutosDao();
            String sql = "select * from produtos";
            PreparedStatement stmt = prodDao.c.prepareStatement(sql);
            rs = stmt.executeQuery();
        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, "Problemas na conexão!\n" + error.toString());
        }
    }
    
    public String converterData (String data) {
        // 2023-06-13
        String ano = data.substring(0, 4); // 2023
        String mes = data.substring(5, 7); // 06
        String dia = data.substring(8, 10); // 13
        String r = dia + "/" + mes + "/" + ano; // 13/06/2023
        return r;
    }
    
    public void carregaDados() {
        try {
            if (rs.isAfterLast()) {
                rs.last();
            }
            if (rs.isBeforeFirst()) {
                rs.first();
            }
            
            jTextFieldId.setText(Integer.toString(rs.getInt("codProd")));
            jTextFieldDescricao.setText(rs.getString("descricao"));
            jTextFieldPreco.setText(Float.toString(rs.getFloat("preco")));
            jTextFieldUnidade.setText(rs.getString("unidade"));
            jTextFieldQuantidadeInicial.setText(Float.toString(rs.getFloat("qtdeInicial")));
            jTextFieldDataCadastro.setText(converterData(rs.getDate("dataCad").toString()));
            jTextFieldQuantidadeAtual.setText(Float.toString(rs.getFloat("qtdeAtual")));
            setBotoes(true,true,false,true,true,true);
        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, "Problemas na conexão!\n" + error.toString());
        }
    }
    

