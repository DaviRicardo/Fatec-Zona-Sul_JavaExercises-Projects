    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        pedDao.pedido.setCodPed(Integer.parseInt(jTextFieldId.getText()));
        pedDao.pedido.setCodCliente(Integer.parseInt(jTextFieldCodigoCliente.getText()));
        
        Date data = new Date();
        String dataString = jTextFieldDataPedido.getText();
        SimpleDateFormat format = new SimpleDateFormat ("dd/MM/yyyy");
        
        try {
            data = format.parse(dataString);
            java.sql.Date dataSql = new java.sql.Date (data.getTime()); // A passagem do valor de timestamp obtido por getTime() ao construtor de java.sql.Date permite criar um objeto java.sql.Date com a mesma data e hora de um objeto do tipo Date 
            pedDao.pedido.setData(dataSql);
        } catch (ParseException error) {
           Logger.getLogger(CadastroPedidos.class.getName()).log(Level.SEVERE, null, error);

        }
        
        JOptionPane.showMessageDialog(null, pedDao.atualizar(PedidosDao.ALTERACAO));
        limparCampos();
        tabelaPedidos();
    }//GEN-LAST:event_jButtonAlterarActionPerformed

