    public void tabelaPedidos () {
        try {
            pedDao = new PedidosDao();
            String sql = "select * from pedidos inner join clientes on clientes.codCliente = pedidos.Clientes_codCliente;";
            PreparedStatement stmt = pedDao.c.prepareStatement(sql);
            rs = stmt.executeQuery();
        } catch (SQLException | ClassNotFoundException error) {
            JOptionPane.showMessageDialog(null, "Problemas na conexão!\n" + error.toString());
        }
    }
    
    public void atualizarCampos() {
        pedDao.pedido.setCodPed(Integer.parseInt(jTextFieldId.getText()));
        if (pedDao.localizar()) {
           // Clientes - Pedidos
           jTextFieldId.setText(Integer.toString(pedDao.pedido.getCodPed()));
           jTextFieldCodigoCliente.setText(Integer.toString(pedDao.pedido.getCodCliente()));
           jTextFieldDataPedido.setText(converterData(pedDao.pedido.getData().toString()));
           // Cliente
           jTextFieldNome1.setText(pedDao.cliente.getNome());
           jTextFieldEndereco1.setText(pedDao.cliente.getEndereco());
           jTextFieldBairro1.setText(pedDao.cliente.getBairro());
           jTextFieldCidade1.setText(pedDao.cliente.getCidade());
           jTextFieldCep1.setText(pedDao.cliente.getCep());
           jTextFieldUf1.setText(pedDao.cliente.getUf());
           jTextFieldEmail1.setText(pedDao.cliente.getEmail());
           jTextFieldFone1.setText(pedDao.cliente.getFone());
           jTextFieldCelular1.setText(pedDao.cliente.getCelular());
           // Pedido
           jTextFieldDataPedido1.setText(converterData(pedDao.pedido.getData().toString()));
           setCamposTexto(true, true, false, false, false, false, false, false, false, false, false, false, false);
           setBotoes(true,true,false,true,true,true);
        } else {
            JOptionPane.showMessageDialog(null, "Pedido não encontrado!", "Falha na localização", JOptionPane.WARNING_MESSAGE);
            limparCampos();
        }
    }
    
    public String converterData (String data) {
        // 2023-06-13
        String ano = data.substring(0, 4); // 2023
        String mes = data.substring(5, 7); // 06
        String dia = data.substring(8, 10); // 13
        String r = dia + "/" + mes + "/" + ano; // 13/06/2023
        return r;
    }
    
    public void carregaDados() {
        try {
            if (rs.isAfterLast()) {
                rs.last();
            } else if (rs.isBeforeFirst()) {
                rs.first();
            }
            // Pedidos - Clientes
            jTextFieldId.setText(Integer.toString(rs.getInt("codPed")));
            jTextFieldCodigoCliente.setText(Integer.toString(rs.getInt("Clientes_codCliente")));
            jTextFieldDataPedido.setText(converterData(rs.getDate("data").toString()));
            // Clientes
            jTextFieldNome1.setText(rs.getString("nome"));
            jTextFieldEndereco1.setText(rs.getString("ender"));
            jTextFieldBairro1.setText(rs.getString("bairro"));
            jTextFieldCidade1.setText(rs.getString("cidade"));
            jTextFieldCep1.setText(rs.getString("cep"));
            jTextFieldUf1.setText(rs.getString("uf"));
            jTextFieldEmail1.setText(rs.getString("email"));
            jTextFieldFone1.setText(rs.getString("fone"));
            jTextFieldCelular1.setText(rs.getString("celular"));
            // Pedido
            jTextFieldDataPedido1.setText(converterData(rs.getDate("data").toString()));
            setBotoes(true,true,false,true,true,true);
            setCamposTexto (true, true, false, false, false, false, false, false, false, false, false, false, false);
        } catch (SQLException error) {
            JOptionPane.showMessageDialog(null, "Erro na conexão!\n" + error.toString());
        }
    }
    
